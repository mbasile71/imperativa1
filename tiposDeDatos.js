// Datos del tipo Number
let edad = 51
console.log(edad)

// Dato del Tipo Number con Decimales
let precio = 150.53
console.log(precio)

//Datos del tipo String
let nombre = "Marcelo"
let apellido = 'Basile'
console.log(nombre)
console.log(apellido)

//Datos Booleanos
let verdadero = true
let falso = false
console.log(verdadero)
console.log(falso)

//Dato del tipo Not a Number NaN
let notANumber = NaN
console.log(notANumber)

//Dato del tipo Null
let nulo = null
console.log(nulo)

//Dato del tipo Undefined
let sinValor
console.log(sinValor)
sinValor = "con Valor"
console.log(sinValor)

//Datos del Tipo Objeto Literal
let miComputadora = {
    marca: 'DELL',
    modelo: "2014",
    ram: "8 GB"
}
console.log(miComputadora)

//Datos del Tipo Array
let comidasFavoritas = ["Asado","Milanesa","Ribs"]
