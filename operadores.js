//Suma
console.log( 5 + 10 )

//Resta
console.log( 10 - 5 )

//Multiplicacion
console.log( 5 * 5 )

//Division
console.log( 40 / 10 )

//Modulo
console.log( 10 % 4 )

//Incremento
let numero = 10
console.log( ++numero )

//Decremento
console.log( --numero )

//Comparacion simple
console.log( 5 == "5" )

//Comparacion Estricta
console.log( 5 === "5" )

//Desigualdad Simple
console.log( 5 != "5")

//Desigualdad Estricta
console.log( 5 !== "5" )

// Mayor que o igual
console.log(  10 >= 10 )

//Menor que o igual
console.log( 10 <=10 ) 

// AND &&  "Para que se cumpla ambas deben ser verdaderas"
console.log( 10 == 10 && 10 < 12 )

// OR ||  "Con cumplirse una de las condiciones sera verdadero"
console.log( 10 == 11 || 10 < 9 )

//Concatenacion
let nombre = "Marcelo"
let apellido = "Basile"
console.log( nombre + " " + apellido )

